		<h2>Как практиковать разговорный английский</h2>
		<p>Мы можем самостоятельно изучать чтение и письмо, смотреть фильмы, новости на английском, это играет немалую роль при изучении English, но для разговоров нам нужен собеседник. Именно в этой части у многих возникают трудности. Наверняка все знают, что самая лучшая и проверенная языковая практика английского языка – это общение с носителем. В идеале окружить себя англоговорящими людьми, например, поехать в путешествие.</p>
		<p>Но что делать, если у вас нет друзей англичан, а поехать за границу нет возможности? Ничего страшного, существует еще масса вариантов, которые позволят прокачать разговорные навыки</p>

